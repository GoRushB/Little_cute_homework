package db;

public class Valkyries {
	private String no;
	private String name;
	private String ratings;
	private int level;
	private String weapon;
	private String upperStigmata;
	private String centreStigmata;
	private String lowerStigmata;
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRatings() {
		return ratings;
	}
	public void setRatings(String ratings) {
		this.ratings = ratings;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public String getWeapon() {
		return weapon;
	}
	public void setWeapon(String weapon) {
		this.weapon = weapon;
	}
	public String getUpperStigmata() {
		return upperStigmata;
	}
	public void setUpperStigmata(String upperStigmata) {
		this.upperStigmata = upperStigmata;
	}
	public String getCentreStigmata() {
		return centreStigmata;
	}
	public void setCentreStigmata(String centreStigmata) {
		this.centreStigmata = centreStigmata;
	}
	public String getLowerStigmata() {
		return lowerStigmata;
	}
	public void setLowerStigmata(String lowerStigmata) {
		this.lowerStigmata = lowerStigmata;
	}
}
